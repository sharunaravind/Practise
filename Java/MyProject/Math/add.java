package Math;

public class add {

    int operand1;
    int operant2;
    public add()
    {
        this.operand1=0;
        this.operant2=0;
    }
    public add(int a,int b)
    {
        this.operand1=a;
        this.operant2=b;
    }
    public static int getSum(int x, int y)
    {
        return x+y;
    }
}
